#include <stdio.h>
int main() {
   // Birinci Boşluğa şubenizi ikinci boşluğa adınızı ve soyadınızı yazın.
   printf("Ben A1 şubesinden Muhammed KESKİN \n");
   // Aşağıdaki satırları yorumlayınız.
   // Burada 65 değerinin desimal karşılğı yazılcak. 
   printf("Çıktı: %d\n", 65);
   // Burada 65 değerinin oktal değeri yazılacak. 
   printf("Çıktı: %o\n", 65);
   // Burada 0101 değerinin desimal değeri yazılacak.
   printf("Çıktı: %d\n", 0101);  
   // Burada 65 değerinin hexdesimal değeri yazılacak.
   printf("Çıktı: %X\n", 65);
   // Burada 0X41 değerinin desimal değeri yazılacak.
   printf("Çıktı: %d\n", 0X41);
   // Burada -65 değerinin İşaretsiz desimal[(işaretsiz tam sayı),(int unsigned decimal)] yazılacak.
   printf("Çıktı: %u\n",-65);  
   // Burada 65.5 değerinin kayan sayılar (a floating point number for floats) değeri yazılacak.
   printf("Çıktı: %f\n",65.5);  
   // Burada 65.5 değerinin bilimsel gösterimde bir kayan noktalı sayı(a floating point number in scientific notation)  değeri yazılacak.
   printf("Çıktı: %e\n", 65.5);  
   // Burada 65.5 değerinin kayan bir noktalı sayı(%e ile benzerlik gösterir) değeri yazılacak.
   printf("Çıktı: %g\n", 65.5);  
   // Burada 65 değerinin Karakter değeri yazılacak.
   printf("Çıktı: %c\n", 65);
   // Burada A değerinin Desimal değeri yazılacak.
   printf("Çıktı: %d\n", 'A');
   return 0 ;
}
